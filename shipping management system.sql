CREATE DATABASE shipping_management_system;
USE shipping_management_system;


CREATE TABLE CUSTOMER(
ID INT PRIMARY KEY,
NAME VARCHAR(40) NOT NULL,
GENDER VARCHAR NOT NULL,
ADDRESS VARCHAR(40) NOT NULL,
CONTACT VARCHAR(10) NOT NULL,
EMAIL VARCHAR(50) NOT NULL);


CREATE TABLE ORDERS(
ID INT PRIMARY KEY,
NAME VARCHAR(60)NOT NULL,
STATUS VARCHAR(30),
ORDER_DATE DATE,
C_ID INT REFERENCES CUSTOMER(ID));


CREATE TABLE DRIVER(
ID INT PRIMARY KEY,
NAME VARCHAR(30) NOT NULL,
LICENSE VARCHAR(30)NOT NULL,
CONTACT_NO VARCHAR(30)NOT NULL);


CREATE TABLE ROUTE(
ID INT PRIMARY KEY,
START_LOCATION VARCHAR(40)NOT NULL,
DESTINATION VARCHAR(30)NOT NULL);


CREATE TABLE VEHICLE(
ID INT PRIMARY KEY,
MODEL VARCHAR(40)NOT NULL,
LICENSE_PLATE VARCHAR(40)NOT NULL,
CAPACITY VARCHAR(30) CHECK(CAPACITY>1000),
DRIVER_ID INT REFERENCES DRIVER(ID),
ROUTE_ID INT REFERENCES ROUTE(ID));


CREATE TABLE SHIPMENTS(
SHIP_ID INT PRIMARY KEY,
SHIP_DATE DATE NOT NULL,
TRACKING_NO VARCHAR(40)NOT NULL ,
DELIVERY_DATE DATE NOT NULL,
ORDER_ID INT REFERENCES ORDERS(ID),
VEHICLE_ID INT REFERENCES VEHICLE(ID));



